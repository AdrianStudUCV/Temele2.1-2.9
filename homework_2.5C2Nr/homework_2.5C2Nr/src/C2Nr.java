public class C2Nr {
    private double n1;
    private double n2;

    public C2Nr(double n1, double n2) {
        this.n1 = n1;
        this.n2 = n2;
    }
    public double getN1() {
        return n1;
    }

    public double getN2() {
        return n2;
    }

    public double getSum() {
        return n1 + n2;
    }
}